import json
from pathlib import Path
from backend.aditya_flare.utils.logger import training_logger

def generate_scientific_report(metrics: dict, output_dir: str):
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    report_path = output_dir / "Scientific_Report.md"
    
    md_content = f"""# Scientific Evaluation Report

## 1. Classification Metrics
- **True Skill Statistic (TSS):** {metrics.get('TSS', 0):.4f}
- **Heidke Skill Score (HSS):** {metrics.get('HSS', 0):.4f}
- **ROC AUC:** {metrics.get('ROC_AUC', 0):.4f}
- **PR AUC:** {metrics.get('PR_AUC', 0):.4f}

## 2. Calibration & Reliability
- **Expected Calibration Error (ECE):** {metrics.get('ECE', 0):.4f}
- **Maximum Calibration Error (MCE):** {metrics.get('MCE', 0):.4f}
- **Brier Score:** {metrics.get('Brier', 0):.4f}

## 3. Operational Metrics
- **False Alarm Rate (FAR):** {metrics.get('FAR', 0)*100:.1f}%
- **True Positive Rate (TPR):** {metrics.get('TPR', 0)*100:.1f}%

### Confusion Matrix
| | Predicted Negative | Predicted Positive |
|---|---|---|
| **Actual Negative** | {metrics.get('TN', 0)} | {metrics.get('FP', 0)} |
| **Actual Positive** | {metrics.get('FN', 0)} | {metrics.get('TP', 0)} |

Generated by Aditya-L1 Flare Nowcasting Pipeline.
"""
    with open(report_path, "w") as f:
        f.write(md_content)
        
    training_logger.info(f"Generated Scientific Report at {report_path}")

def generate_benchmark_report(models_metrics: dict, output_dir: str):
    """
    models_metrics is a dict where key is model name, value is metrics dict.
    """
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    report_path = output_dir / "Benchmark_Report.md"
    
    md_content = "# Model Benchmark Comparison\\n\\n"
    md_content += "| Model | TSS | HSS | ROC AUC | ECE | FAR |\\n"
    md_content += "|---|---|---|---|---|---|\\n"
    
    for model_name, metrics in models_metrics.items():
        md_content += f"| {model_name} | {metrics.get('TSS',0):.3f} | {metrics.get('HSS',0):.3f} | {metrics.get('ROC_AUC',0):.3f} | {metrics.get('ECE',0):.3f} | {metrics.get('FAR',0)*100:.1f}% |\\n"
        
    with open(report_path, "w") as f:
        f.write(md_content)
        
    training_logger.info(f"Generated Benchmark Report at {report_path}")
